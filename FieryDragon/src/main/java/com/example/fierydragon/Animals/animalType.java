package com.example.fierydragon.Animals;

/**
 * The animalType enum represents various types of animals that can be encountered in the game.
 */
public enum animalType {
    BAT,         // Represents a bat
    SPIDER,      // Represents a spider
    DRAGON,      // Represents a dragon
    LIZARD,      // Represents a lizard
    DRAGON_PIRATE, // Represents a dragon pirate (skeleton)
    REVERSE_DRAGON,
    THIEF
}
